    private void buscarRecursos() {
      
                
        this.daoService = new DaoService();

        
        
            Equipamento temp = new Equipamento();
            temp.setNome(txtNome.getText());
        
            listaRecurso= (ArrayList<Equipamento>) daoService.select(temp);
                     
            
            DefaultTableModel mdl = (DefaultTableModel) jTable1.getModel();
            mdl.setNumRows(0);
            
            for(int i = 0 ; i <listaRecurso.size();i++){
                
                Equipamento rec = listaRecurso.get(i);
                mdl.addRow(new Object[]{rec.getId(),rec.getNome()});
           }
            
            
            
            
     
        if(recurso!=null){
            loadAtividade();
        }
        
    }

    private void selecionarRecurso() {
       
            int row = jTable1.getSelectedRow();
            recurso = listaRecurso.get(row);
        
            
            this.recurso = (Equipamento) daoService.find(recurso, recurso.getId());
            
            
            
            txtId.setText(""+recurso.getId());
            txtNome1.setText(recurso.getNome());
            txtDescr.setText(recurso.getDescricao());
            if(recurso.isStatus()){
                txtStatus.setText("ATIVO");
            }else{
                txtStatus.setText("INATIVO");
            }
          loadAtividade();
            
            
            
            
    
      
    }

