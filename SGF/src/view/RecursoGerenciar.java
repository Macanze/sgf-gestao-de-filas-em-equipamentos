    private void removerAssociacao()  {
       daoService.begin();
      int row = jTable2.getSelectedRow();
        Atividade get = (Atividade) listaAtividadeAssociada.toArray()[row];
        boolean remove = this.recurso.getListaAtividades().remove(get);
          
        JOptionPane.showMessageDialog(null, remove);
        daoService.commit();
           
              
            
        
        loadAtividade();
    }

    private void loadAtividade() {
        
    
        this.listaAtividadeAssociada = recurso.getListaAtividades();
            
                
                DefaultTableModel mdl = (DefaultTableModel) jTable2.getModel();
                mdl.setNumRows(0);
        for (Iterator<Atividade> it = listaAtividadeAssociada.iterator(); it.hasNext();) {
            Atividade get = it.next();
            mdl.addRow(new Object[]{get.getId(),get.getNome()});
        }
     
     
        
    }
